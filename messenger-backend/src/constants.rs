//! Магические числа в одном месте, чтобы при тюнинге не искать их
//! по всему дереву модулей.

/// Сколько узлов минимум в onion-цепочке (guard, middle, exit).
/// Меньше трёх — не имеет смысла называть это onion routing, это
/// просто прокси с одним хопом.
pub const ONION_MIN_HOPS: usize = 3;

/// Максимум узлов в цепочке — больше не даёт прироста анонимности,
/// но увеличивает задержку линейно.
pub const ONION_MAX_HOPS: usize = 5;

/// Все onion-пакеты паддятся до этого размера, чтобы relay не мог
/// отличить типы сообщений (текст/фото/системное) по длине пакета.
pub const ONION_CELL_SIZE: usize = 4096;

/// TTL записи в DHT (prekey bundle, mailbox-адрес) — после этого узел
/// обязан переопубликовать запись, иначе она вымывается из сети.
pub const DHT_RECORD_TTL_SECS: u64 = 60 * 60 * 24; // 24 часа

/// Через сколько переопубликовывать запись — с запасом до истечения TTL,
/// чтобы не было "дыры" если узел был оффлайн в момент публикации.
pub const DHT_REPUBLISH_INTERVAL_SECS: u64 = 60 * 60 * 6; // 6 часов

/// Сколько сообщений максимум лежит в mailbox одного пользователя —
/// защита от переполнения хранилища relay-узла спамом на оффлайн-юзера.
pub const MAILBOX_MAX_MESSAGES_PER_USER: usize = 500;

/// Сколько дней недоставленное сообщение живёт в mailbox прежде чем
/// его удалит cleanup.
pub const MAILBOX_MESSAGE_TTL_DAYS: i64 = 30;

/// Сколько one-time prekeys публикуется за раз — заранее заготовленный
/// пул, чтобы можно было начать сессию с оффлайн-пользователем без
/// дополнительного round-trip.
pub const PREKEY_BATCH_SIZE: usize = 100;

/// Ниже этого количества оставшихся one-time prekeys в DHT — пора
/// публиковать новую партию.
pub const PREKEY_LOW_WATERMARK: usize = 10;

/// Сколько раз подряд relay должен смолчать/зафейлить прежде чем
/// scoring исключит его из выбора для новых цепочек.
pub const RELAY_FAILURE_THRESHOLD: u32 = 5;

/// Таймаут ожидания ответа от relay/DHT-запроса.
pub const NETWORK_REQUEST_TIMEOUT_SECS: u64 = 20;

/// Максимальный размер одного сообщения (после чего — chunking).
pub const MAX_MESSAGE_SIZE_BYTES: usize = 64 * 1024;
